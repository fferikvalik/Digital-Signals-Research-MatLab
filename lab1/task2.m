% Период сигнала
N = 72;

% Определение значений n для дискретного сигнала
n = 0:N-1;  % 72 точки для одного полного периода

% Частота дискретного сигнала (1/N - соответствует одному периоду за N точек)
omega = 2 * pi / N;

% Определение дискретного синусоидального сигнала
x = sin(omega * n);

% Построение графика дискретного сигнала
hold on
stem(n, x);  % Используем stem для отображения дискретных значений
plot(n, x, 'b', 'LineWidth', 1.5); % Линия соединяет точки
title('Дискретный синусоидальный сигнал с периодом N = 72');
xlabel('n (дискретное время)');
ylabel('Амплитуда');
grid on;
 
