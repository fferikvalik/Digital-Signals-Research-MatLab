% Заданные параметры
fs = 16000;   % Частота дискретизации
fa = 2;       % Частота АМ-сигнала
ka = 0.5;     % Коэффициент амплитудной модуляции
r = 0.4;      % Параметр смещения синусоиды
t = 0:1/fs:2; % Время, 2 секунды

% Генерация смещённой синусоиды Sb(n)
Sb = sin(2 * pi * fa * t + r);

% Генерация АМ-сигнала
x = (1 + ka * Sb) .* cos(2 * pi * 1000 * t); % f_c = 1000 Гц

% Сохранение сигнала
audiowrite('am_skew_sin.wav', x, fs);

% Вариант с ka = 0
x_no_mod = cos(2 * pi * 1000 * t); % Без модуляции
audiowrite('am_no_mod.wav', x_no_mod, fs);
