function [t, s] = generate_sine(A, f0, phi0, fs, t0, t1)
    % A - амплитуда сигнала
    % f0 - частота синусоиды (кГц)
    % phi0 - начальная фаза (радианы)
    % fs - частота дискретизации (кГц)
    % t0 - начальное время (с)
    % t1 - конечное время (с)
    
    % Переход в единицы Гц
    f0 = f0 * 1e3;  % Частота синусоиды в Гц
    fs = fs * 1e3;  % Частота дискретизации в Гц
    
    % Определение временных отметок с более крупным шагом для улучшения графика
    t = t0:1/(fs*10):t1;  % Время дискретизации с уменьшенным шагом
    
    % Вычисление значений синусоиды
    s = A * sin(2 * pi * f0 * t + phi0);
    
    % Построение графика
    figure;
    plot(t, s, '-o');  % Добавляем маркеры для точек
    title('Дискретизированный синусоидальный сигнал');
    xlabel('Время (с)');
    ylabel('Амплитуда');
    grid on;
    
    % Настройка осей
    axis([t0 t1 -A A]);  % Устанавливаем границы осей
end
