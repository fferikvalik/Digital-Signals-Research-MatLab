% Значение N
N = 51;

% Диапазон значений n
n = 0:N-1;

% Вычисление функции m1(n)
term = (8*n - 4*N) / N;  % выражение внутри скобок
m1 = (1 - term.^2) .* exp(-term.^2 / 2);

% Построение графика
figure;
plot(n, m1, 'b', 'LineWidth', 2);
title('График функции m_1(n)');
xlabel('n');
ylabel('m_1(n)');
grid on;
